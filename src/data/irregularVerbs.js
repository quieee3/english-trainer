export function shuffle(arr) {
  const a = [...arr]
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]]
  }
  return a
}

export function getVerbPool(maxLevel) {
  return VERBS.filter(v => v.lv <= maxLevel)
}

export function checkAnswer(input, correct) {
  const val = input.trim().toLowerCase()
  const accepted = correct.toLowerCase().split('/')
  return accepted.includes(val)
}
